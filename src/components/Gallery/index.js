const SelectedImage = props => {
  const {imageDetails} = props
  const {imageUrl, imageAltText, id} = imageDetails

  return (
    <div>
      <img src={imageUrl} alt={imageAltText} className="selectedImageSize" />
    </div>
  )
}

class Gallery extends Component {
  state = {activeId: 0}

  changePhoto = id => {
    const {activeId} = this.state
    this.setState({activeId: id})
  }

  render() {
    const {activeId} = this.state
    console.log(imagesList[activeId])
    return (
      <div className="main-con">
        <h1 className="head">Nature Photography</h1>
        <p className="sub-head">Nature Photography by Rahul</p>
        <SelectedImage imageDetails={imagesList[activeId]} key={activeId} />
        <ul className="thumbnail-con">
          {imagesList.map(imageDetails => (
            <ThumbnailItem
              key={imageDetails.id}
              imageDetails={imageDetails}
              changePhoto={this.changePhoto}
              isActive={activeId === imageDetails.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}
export default Gallery